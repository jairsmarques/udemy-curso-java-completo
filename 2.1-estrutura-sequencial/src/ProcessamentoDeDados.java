
public class ProcessamentoDeDados {

	public static void main(String[] args) {

		// <variável> = <expressão>;
		int x, y;
		x = 5;
		y = 2 * x;

		System.out.println(x);
		System.out.println(y);

		System.out.println("---------------");
		int i;
		double j;

		i = 5;
		j = 2 * i;
		System.out.println(i);
		System.out.println(j);

		System.out.println("---------------");

	}

}
